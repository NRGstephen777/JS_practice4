let a = 10;
let b = 5;

substraction()
multiplication()
division()
addition()


function substraction(){
    c = a - b
    console.log(`Substraction: ${a} - ${b} = ${c}`)
}

function multiplication(){
    c = a * b
    console.log(`Multiplication: ${a} * ${b} = ${c}`)
}

function division(){
    c = a / b
    console.log(`Division: ${a} / ${b} = ${c}`)
}

function addition(){
    c = a + b
    console.log(`Addition: ${a} + ${b} = ${c}`)
}